//.log() function is used to print to the console
console.log("Hello world");
console.log("Tutorial on JS");
console.log([34, 5, 7, 15]);
console.log({ name: "Ayush", marks: 34 });

//.table() is used to print an object in a table
console.table({ name: "Ayush", marks: 34 });

//.warn() is used for warnings
console.warn("This is a warning");

//.clear() is used for clearing the screen
//.time() and .timeEnd() are used to wrap code snippets and find the time required for executing.

//.assert(condition, statement) is used to print the statement if the condition is true

let age = 192;
console.assert(age > 189, "This is not possible");
